/**
 * @brief Reset the display via TCA9554 I/O expander.
 *
 * MUST be called before esp_lcd_panel_init(). The display reset line is
 * routed through TCA9554 (I2C 0x20, pin 1) on the 3.5B-C board.
 *
 * Assumes I2C bus is already initialized on BOARD_TCA9554_I2C_PORT.
 *
 * @return ESP_OK on success
 */
static inline esp_err_t board_display_35bc_tca9554_reset(void)
{
    const char *TAG_RST = "35bc_reset";
    uint8_t reg_val = 0;

    /* Read current output register (0x01) */
    uint8_t cmd = 0x01;
    i2c_cmd_handle_t handle = i2c_cmd_link_create();
    i2c_master_start(handle);
    i2c_master_write_byte(handle, (BOARD_TCA9554_I2C_ADDR << 1) | I2C_MASTER_WRITE, true);
    i2c_master_write_byte(handle, cmd, true);
    i2c_master_start(handle);
    i2c_master_write_byte(handle, (BOARD_TCA9554_I2C_ADDR << 1) | I2C_MASTER_READ, true);
    i2c_master_read_byte(handle, &reg_val, I2C_MASTER_NACK);
    i2c_master_stop(handle);
    esp_err_t ret = i2c_master_cmd_begin(BOARD_TCA9554_I2C_PORT, handle, pdMS_TO_TICKS(100));
    i2c_cmd_link_delete(handle);
    if (ret != ESP_OK) {
        ESP_LOGE(TAG_RST, "TCA9554 read failed: %s", esp_err_to_name(ret));
        return ret;
    }

    /* Pull pin 1 LOW (assert reset) */
    uint8_t data[2] = {0x01, reg_val & ~(1 << BOARD_TCA9554_RST_PIN)};
    handle = i2c_cmd_link_create();
    i2c_master_start(handle);
    i2c_master_write_byte(handle, (BOARD_TCA9554_I2C_ADDR << 1) | I2C_MASTER_WRITE, true);
    i2c_master_write(handle, data, 2, true);
    i2c_master_stop(handle);
    ret = i2c_master_cmd_begin(BOARD_TCA9554_I2C_PORT, handle, pdMS_TO_TICKS(100));
    i2c_cmd_link_delete(handle);
    if (ret != ESP_OK) {
        ESP_LOGE(TAG_RST, "TCA9554 write (reset low) failed: %s", esp_err_to_name(ret));
        return ret;
    }

    vTaskDelay(pdMS_TO_TICKS(20));

    /* Pull pin 1 HIGH (release reset) */
    data[1] = reg_val | (1 << BOARD_TCA9554_RST_PIN);
    handle = i2c_cmd_link_create();
    i2c_master_start(handle);
    i2c_master_write_byte(handle, (BOARD_TCA9554_I2C_ADDR << 1) | I2C_MASTER_WRITE, true);
    i2c_master_write(handle, data, 2, true);
    i2c_master_stop(handle);
    ret = i2c_master_cmd_begin(BOARD_TCA9554_I2C_PORT, handle, pdMS_TO_TICKS(100));
    i2c_cmd_link_delete(handle);
    if (ret != ESP_OK) {
        ESP_LOGE(TAG_RST, "TCA9554 write (reset high) failed: %s", esp_err_to_name(ret));
        return ret;
    }

    vTaskDelay(pdMS_TO_TICKS(120));

    ESP_LOGI(TAG_RST, "Display reset via TCA9554 complete");
    return ESP_OK;
}

